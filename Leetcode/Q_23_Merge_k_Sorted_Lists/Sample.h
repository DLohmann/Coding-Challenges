#ifndef Sample_H_
#define Sample_H_

#include<vector>

namespace sample {

struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};

class Solution {
public:
    ListNode* mergeKLists(std::vector<ListNode*>& lists);
};

}  // namespace sample

#endif  // Sample_H_
